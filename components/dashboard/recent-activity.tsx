// components/dashboard/recent-activity.tsx
import {
  Card,
  CardContent,
  CardDescription,
  CardHeader,
  CardTitle,
} from "@/components/ui/card";

interface Activity {
  id: string;
  title: string;
  description: string;
  timestamp: string;
  type: "appointment" | "record" | "payment" | "medicine" | "other";
}

interface RecentActivityProps {
  activities: Activity[];
  loading?: boolean;
}

export function RecentActivity({
  activities,
  loading = false,
}: RecentActivityProps) {
  return (
    <Card className="col-span-3">
      <CardHeader>
        <CardTitle>Recent Activity</CardTitle>
        <CardDescription>
          Recent actions and events in the system
        </CardDescription>
      </CardHeader>
      <CardContent>
        {loading ? (
          <div className="space-y-4">
            {Array.from({ length: 5 }).map((_, i) => (
              <div key={i} className="flex items-center space-x-4">
                <div className="h-10 w-10 rounded-full bg-muted animate-pulse" />
                <div className="space-y-2">
                  <div className="h-4 w-40 rounded-md bg-muted animate-pulse" />
                  <div className="h-3 w-24 rounded-md bg-muted animate-pulse" />
                </div>
              </div>
            ))}
          </div>
        ) : activities.length === 0 ? (
          <p className="text-sm text-muted-foreground">No recent activity</p>
        ) : (
          <div className="space-y-4">
            {activities.map((activity) => (
              <div key={activity.id} className="flex items-start space-x-4">
                <div className="rounded-full p-2 bg-primary/10">
                  {getActivityIcon(activity.type)}
                </div>
                <div>
                  <p className="text-sm font-medium leading-none">
                    {activity.title}
                  </p>
                  <p className="text-sm text-muted-foreground">
                    {activity.description}
                  </p>
                  <p className="text-xs text-muted-foreground">
                    {activity.timestamp}
                  </p>
                </div>
              </div>
            ))}
          </div>
        )}
      </CardContent>
    </Card>
  );
}

function getActivityIcon(type: Activity["type"]) {
  switch (type) {
    case "appointment":
      return <CalendarIcon className="h-4 w-4 text-primary" />;
    case "record":
      return <FileTextIcon className="h-4 w-4 text-primary" />;
    case "payment":
      return <DollarSignIcon className="h-4 w-4 text-primary" />;
    case "medicine":
      return <PillIcon className="h-4 w-4 text-primary" />;
    default:
      return <ActivityIcon className="h-4 w-4 text-primary" />;
  }
}

